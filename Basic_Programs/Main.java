class Student {
    String name;
    int age;

    // Constructor
    Student(String n, int a) {
        name = n;
        age = a;
    }

    void display() {
        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
    }
}

public class Main {
    public static void main(String[] args) {
        // constructor automatically sets values when we create the object
        Student s1 = new Student("Chaitanya", 21);
        s1.display();
    }
}
